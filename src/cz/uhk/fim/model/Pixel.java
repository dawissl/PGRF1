package cz.uhk.fim.model;

/**
 * @author David Sladecek
 */

public class Pixel {

    public int x;
    public int y;

    public Pixel(int x, int y) {
        this.x = x;
        this.y = y;
    }


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
